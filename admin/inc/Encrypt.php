			'10' => '202020',
			'11' => '303030',
			'12' => '404040',
			'13' => '101010',
			'14' => '202020',
			'15' => '303030',
			'0' => '404040'
		);
				
		return md5($data['value'].$salt[$data['salt']]);
		
	}	
}

